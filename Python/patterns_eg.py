#square
for x in range(1,4):
    for y in range(1,4):
        print('*',end='')
    print()

print()

#left triangle
for c in range(1,6):
    for d in range(c):
        print('*',end='')
    print()

print()

#reverse triangle
for e in range(5,0,-1):
    for f in range(e):
        print('*',end='')
    print()

print()

#number traiangle
for g in range(1,6):
    for h in range(g):
        print(g,end='')
    print()

print()

#increasing numbers
for u in range(1,6):
    for v in range(1,u+1):
        print(v,end='')
    print()

print()

#diamond half
for i in range(1,6):
    for j in range(i):
        print('*',end='')
    print()
for i in range(4,0,-1):
    for j in range(i):
        print('*',end='')
    print()

print()

